
public interface Shape {
	enum ShapeType {
		Square,
		Circle,
		Rectangle
	}
	String getTimeStamp();
	/**
	 * 
	 * @return type of shape
	 */
	String shapeType();
	/**
	 * 
	 * @return area of the given shape
	 */
	double getArea();
	/**
	 * 
	 * @return perimeter of the given shape
	 */
	double getPerimeter();
	/**
	 * 
	 * @return coordinate of origin of the given shape
	 */
	Point getOrigin();
	/**
	 * 
	 * @return true of point is within or on 
	 * 		   boundary of shape,false in other cases
	 */
	boolean isPointEnclosed(Point checkPoint);
}
