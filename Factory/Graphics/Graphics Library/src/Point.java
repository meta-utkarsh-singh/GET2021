
public class Point {
	private double x;
	private double y;
	Point(double x, double y) {
		this.x = x;
		this.y = y;
	}
	/**
	 * 
	 * @return x coordinate of point
	 */
	public double getX(){
		return this.x;
	}
	/**
	 * 
	 * @return y coordinate of point
	 */
	public double getY(){
		return this.y;
	}
}
